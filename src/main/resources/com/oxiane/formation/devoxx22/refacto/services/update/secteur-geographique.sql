# create tables for secteur geographique

DELIMITER ;

CREATE TABLE SECTEUR_GEOGRAPHIQUE (
    ID BIGINT NOT NULL PRIMARY KEY,
    NOM VARCHAR(48) NOT NULL,
    COEF_MULTI NUMERIC(7,4) DEFAULT 1 NOT NULL
);

CREATE TABLE DEPARTEMENT (
    CODE VARCHAR(3) NOT NULL PRIMARY KEY,
    SECTEUR_GEO BIGINT,
    CONSTRAINT SECT_GEO_FK FOREIGN KEY(SECTEUR_GEO) REFERENCES SECTEUR_GEOGRAPHIQUE ON DELETE SET NULL
);

